// ==================== 工具函数 ====================

/**
 * 在网格中放置指定图案
 */
function placePattern(grid, patternId, centerCol, centerRow, teamId = 1) {
  const pattern = PATTERN_LIBRARY[patternId]
  if (!pattern) return

  const rows = grid.length
  const cols = grid[0].length

  // 随机汤特殊处理
  if (pattern.cells === 'random') {
    const size = pattern.randomSize || 10
    const density = pattern.randomDensity || 0.4
    const half = Math.floor(size / 2)
    for (let dy = -half; dy < half; dy++) {
      for (let dx = -half; dx < half; dx++) {
        if (Math.random() < density) {
          const col = centerCol + dx
          const row = centerRow + dy
          if (row >= 0 && row < rows && col >= 0 && col < cols) {
            grid[row][col] = teamId
          }
        }
      }
    }
    return
  }

  for (const [dx, dy] of pattern.cells) {
    const col = centerCol + dx
    const row = centerRow + dy
    if (row >= 0 && row < rows && col >= 0 && col < cols) {
      grid[row][col] = teamId
    }
  }
}
