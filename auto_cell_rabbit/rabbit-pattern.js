/**
 * 兔子像素图案定义
 * 严格参考用户提供的像素图
 */

// 兔子图案 - 相对坐标系统 [dx, dy]
// 原点在兔子中心
const RABBIT_PATTERN = {
  // 左耳（2格宽×6格高）
  leftEar: [
    [-3, -12], [-2, -12],
    [-3, -11], [-2, -11],
    [-3, -10], [-2, -10],
    [-3, -9], [-2, -9],
    [-3, -8], [-2, -8],
    [-3, -7], [-2, -7],
  ],
  
  // 右耳（2格宽×6格高）
  rightEar: [
    [1, -12], [2, -12],
    [1, -11], [2, -11],
    [1, -10], [2, -10],
    [1, -9], [2, -9],
    [1, -8], [2, -8],
    [1, -7], [2, -7],
  ],
  
  // 脸部（圆形，约8格宽×8格高）
  face: [
    // 顶部
    [-2, -6], [-1, -6], [0, -6], [1, -6],
    // 第二行
    [-3, -5], [-2, -5], [-1, -5], [0, -5], [1, -5], [2, -5],
    // 中部（最宽）
    [-4, -4], [-3, -4], [-2, -4], [-1, -4], [0, -4], [1, -4], [2, -4], [3, -4],
    [-4, -3], [-3, -3], [-2, -3], [-1, -3], [0, -3], [1, -3], [2, -3], [3, -3],
    [-4, -2], [-3, -2], [-2, -2], [-1, -2], [0, -2], [1, -2], [2, -2], [3, -2],
    [-4, -1], [-3, -1], [-2, -1], [-1, -1], [0, -1], [1, -1], [2, -1], [3, -1],
    // 下部
    [-3, 0], [-2, 0], [-1, 0], [0, 0], [1, 0], [2, 0],
    [-2, 1], [-1, 1], [0, 1], [1, 1],
  ],
  
  // 左眼（单点）
  leftEye: [
    [-2, -3],
  ],
  
  // 右眼（单点）
  rightEye: [
    [1, -3],
  ],
  
  // 鼻子（单点或小三角）
  nose: [
    [0, -1],
  ],
}

/**
 * 获取完整兔子图案的坐标数组
 */
function getRabbitPattern() {
  const allCells = []
  for (const part in RABBIT_PATTERN) {
    allCells.push(...RABBIT_PATTERN[part])
  }
  return allCells
}

/**
 * 将相对坐标转换为网格坐标
 */
function getRabbitGridPositions(centerCol, centerRow) {
  const pattern = getRabbitPattern()
  return pattern.map(([dx, dy]) => [centerCol + dx, centerRow + dy])
}
